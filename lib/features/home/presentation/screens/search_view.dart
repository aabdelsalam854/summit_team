        // if (width <= 600) {
        //   crossAxisCount = 1;
        // } else if (width <= 800) {
        //   crossAxisCount = 2;
        // } else {
        //   crossAxisCount = 3;
        // }
        int crossAxisCount;

        if (width <= 600) {
          crossAxisCount = 1;
        } else if (width <= 800) {
          crossAxisCount = 2;
        } else if (width <= 1000) {
          crossAxisCount = 3;
        } else if (width <= 1200) {
          crossAxisCount = 3;
        } else if (width <= 1400) {
          crossAxisCount = 4;
        } else if (width <= 1600) {
          crossAxisCount = 5;
        } else if (width <= 1800) {
          crossAxisCount = 6;
        } else if (width <= 2000) {
          crossAxisCount = 7;
        } else if (width <= 2200) {
          crossAxisCount = 8;
        } else {
          crossAxisCount = 10;
        }

        return Padding(
          padding: const EdgeInsets.symmetric(horizontal: 48),
          child: GridView.builder(
            shrinkWrap: true,
            physics: const NeverScrollableScrollPhysics(),
            gridDelegate: SliverGridDelegateWithFixedCrossAxisCount(
              crossAxisCount: crossAxisCount,
              crossAxisSpacing: 24,
              mainAxisSpacing: 24,
              childAspectRatio: width <= 600 ? 1.2 : (width <= 1200 ? 1 : .8),
            ),
            itemCount: featured.length,
            itemBuilder: (context, index) {
              return PropertyCardWidget(
                property: featured[index],
                onTap: () {},
              );
            },
          ),
        );
      },
    );
  }
